n = 15
dp = [0] * n # Инициализация массива

# База динамического программирования
dp[0] = 1 
dp[1] = dp[0]
dp[2] = dp[0] + dp[1]
print(dp[:3])
# dp[:3] -> [1, 1, 2]

for i in range(3, n): # Обход массив - слева направо, начиная с индекса 3
    dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3] # Формула для пересчета

# dp[n - 1] = Количество способов добраться в последнюю клетку
print(dp[n - 1])
# dp[n - 1] -> 3136
